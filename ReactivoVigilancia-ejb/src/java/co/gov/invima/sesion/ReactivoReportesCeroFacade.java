import co.gov.invima.entities.ReactivoReportesCero;
import java.util.List;
import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.persistence.Query;
/**************************************************************************/
/**************************************************************************/
/**************************************************************************/
/**************************************************************************/
/**************************************************************************/
/**************************************************************************/
/**
 *
 * @author Jaime Alberto Gutiérrez Mejía
 */
@Stateless
public class ReactivoReportesCeroFacade implements ReactivoReportesCeroFacadeLocal 
{
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @PersistenceContext(unitName = "ReactivoVigilancia-ejbPU")
    private EntityManager em;
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @Override
    public void create(ReactivoReportesCero tecnoReportesCero) 
    {
        em.persist(tecnoReportesCero);
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @Override
    public void edit(ReactivoReportesCero tecnoReportesCero) 
    {
        em.merge(tecnoReportesCero);
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @Override
    public void remove(ReactivoReportesCero tecnoReportesCero) 
    {
        em.remove(em.merge(tecnoReportesCero));
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @Override
    public ReactivoReportesCero find(Object id) 
    {
        return em.find(ReactivoReportesCero.class, id);
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    @Override
    public List<ReactivoReportesCero> findAll() 
    {
        return em.createQuery("select object(o) from ReactivoReportesCero as o").getResultList();
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //public long buscarMaxId()
    @Override
    public int buscarMaxId()
    {
        //long respuesta;
        int respuesta;
            
        Query q = em.createQuery("SELECT max(tr.idregistro) from ReactivoReportesCero as tr");
         if(q.getSingleResult()==null)
            respuesta=0;
        else
            respuesta =(int)q.getSingleResult();
        
         return respuesta;
    }
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
    //**********************************************************************
}
